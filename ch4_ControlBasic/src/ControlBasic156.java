
// 반복문 - for
public class ControlBasic156 {

	public static void main(String[] args) {
		
//		반복문은 어떤 작업이 반복적으로 수행되도록 할 때 사용되며,
//		주어진 조건을 만족하는 동안 주어진 문장들을 반복적으로 수행하므로
//		조건식을 포함하며, if문과 마찬가지로 조건식의 결과가 true이면
//		참이고, false면 거짓으로 간주된다
//		
//		*for문은 주로 반복횟수를 알고 있을 때 사용한다
//		
//		expression
//			1		2		4		
//		for(초기화; 조건식; 증감식) {
//			3
//			조건식이 참일 때 수행될 문장들을 적는다
//		}
		
		for(int i=1; i <= 5; i++) {
			System.out.println(i);
		}
		
		
	}
	
}
